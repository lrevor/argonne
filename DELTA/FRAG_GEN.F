C     THIS SUBROUTINE IS TO TAKE THE SEQUENCE AND BREAK IT UP INTO FRAGMENTS
C     M-NUMBER OF FRAGMENTS   N-LENGTH OF FRAGMENTS   
C     ENDPT-TWO DIMENSIONAL ARRAY OF LENGTH M
C     FRAG-RESULTING ARRAY OF FRAGMENTS

      SUBROUTINE FRAG_GEN (M, MAXNUM, N, LOW, HIGH,
     1                     FRAG, SEQ_LEN)

      INTEGER M, MAXNUM, N, LOW, HIGH, FRAG(M), SEQ_LEN
      REAL SEED

      FRAG(1) = 1
      M = 2
   10 SEED = RRAND(0.)
      FRAG(M) = INT(SEED*REAL(HIGH-LOW+1)) + LOW + FRAG(M-1)
      IF ((FRAG(M)+N) .GT. SEQ_LEN) THEN
        FRAG(M) = SEQ_LEN-N+1
       ELSE
        M = M+1
        GO TO 10
      END IF

      RETURN
      END
