C     THIS IS A SUBROUTINE TO GENERATE THE SEQUENCE RANDOMLY.

      SUBROUTINE SEQ_GEN (SCOUNT, SEQ, SEED, WORDLEN)

      INTEGER SCOUNT, SEQ(SCOUNT), WORDLEN

      INTEGER LOOP, LOOP2, VALUE, TEMP
      REAL SEED

      TEMP = RRAND(SEED)

      DO 10 LOOP = 1, SCOUNT
       VALUE=0
       DO 20 LOOP2 = 0, WORDLEN-4, 2
        TEMP = (RRAND(0.)*4.0)
        TEMP = TEMP*2**LOOP2
        VALUE = VALUE.OR.TEMP
  20   CONTINUE
       SEQ(LOOP)=VALUE
  10  CONTINUE

      RETURN
      END
